def generate(generated):
    generated.append(r"\documentclass[fleqn]{article}")
    generated.append(r"\usepackage{graphicx}")
    generated.append(r"\usepackage{./mydefs}")
    generated.append(r"\usepackage{./notes}")
    generated.append(r"\usepackage{url}")
    generated.append(r"\usepackage{float}")
    generated.append(r"\usepackage{amsmath}")
    generated.append(r"\usepackage{listings}")
    generated.append(r"\usepackage{courier}")
    generated.append(r"\usepackage{subcaption}")
    generated.append(r"\usepackage{tcolorbox}")
    generated.append(r"\usepackage{color}")
    generated.append(r"\definecolor{codegreen}{rgb}{0,0.6,0}")
    generated.append(r"\definecolor{codegray}{rgb}{0.5,0.5,0.5}")
    generated.append(r"\definecolor{codepurple}{rgb}{0.58,0,0.82}")
    generated.append(r"\definecolor{backcolour}{rgb}{0.95,0.95,0.92}")
    generated.append(r"\lstdefinestyle{mystyle}{")
    generated.append(r"backgroundcolor=\color{backcolour},")
    generated.append(r"commentstyle=\color{codegreen},")
    generated.append(r"keywordstyle=\color{magenta},")
    generated.append(r"numberstyle=\tiny\color{codegray},")
    generated.append(r"stringstyle=\color{codepurple},")
    generated.append(r"basicstyle=\ttfamily\footnotesize,")
    generated.append(r"breakatwhitespace=false,")
    generated.append(r"breaklines=true,")
    generated.append(r"captionpos=b,")
    generated.append(r"keepspaces=true,")
    generated.append(r"numbers=left,")
    generated.append(r"numbersep=5pt,")
    generated.append(r"showspaces=false,")
    generated.append(r"showstringspaces=false,")
    generated.append(r"showtabs=false,")
    generated.append(r"tabsize=2")
    generated.append(r"}")
    generated.append(r"\lstset{style=mystyle}")
    generated.append(r"\begin{document}")